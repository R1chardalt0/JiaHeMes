/** 登录接口 POST /api/login/account */
export async function login(body: UserLoginDto, options?: { [key: string]: any }) {
  const response = await request<LoginResponse>('/api/Login/CheckLogin', {
    method: 'POST',
    headers: {
      'Content-Type': 'application/json',
    },
    data: body,
    ...(options || {}),
  });
  // 添加Token存储逻辑
  if (response.token) {
    localStorage.setItem('authToken', response.token);
    // 同步写入 Cookie，便于跨标签页与 SSR/代理读取
    setTokenCookie(response.token);
  }
  return response;
}

